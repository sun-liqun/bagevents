public class AnimPoint {

    public AnimPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    float x;
    float y;

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }
}
